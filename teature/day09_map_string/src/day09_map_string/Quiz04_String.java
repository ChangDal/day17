package day09_map_string;

public class Quiz04_String {
	public static void main(String [] args){
		String str = "김개똥 -2017년\n홍길동구리 -2015년\n선우선녀 -2018년";
		System.out.println("====변경 전 str====\n"+str);
		//str = "김개똥 :2017년\n홍길동구리 :2015년\n선우선녀 :2018년";
		str = str.replace("-", ":");
		//arrStr = ["김개똥 :2017년","홍길동구리 :2015년","선우선녀 :2018년"]
		String[] arrStr = str.split("\n");
		str="";
		for(int i=0;i<arrStr.length;i++) {
			//reStr = ["홍길동구리 ","2015년"]
			String[] reStr = arrStr[i].split(":");
			//reStr = ["홍길동구리 ","1999년"]
			reStr[1]=reStr[1].replace(reStr[1], "1999년");
			//reStr[1]="1999년"; //위의식이나 현재 줄의 식이나 동일하다
			//str = "김개똥 :1999년\n홍길동구리 :1999년\n"
			str+=(reStr[0]+":"+reStr[1]+"\n");
		}
		System.out.println("====변경 후 str====\n"+str);
	}

}
